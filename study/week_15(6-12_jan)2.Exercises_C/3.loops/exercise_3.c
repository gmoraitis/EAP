/*ΑΣΚΗΣΗ 3
Να γράψετε πρόγραμμα που να εμφανίζει την προπαίδεια 
όλων των αριθμών από το 0 έως και το 9 (π.χ. για το 4 να εμφανίζει 1x4=4, 2x4=8,... , 10x4=40).*/

#include <stdio.h>
int main(void)
{
    int i, k;
    for (k = 1; k <= 10; k++)
    {
        for (i = 1; i <= 10; i++)
            printf("%dx%d=%d\n", i, k, i * k);
        printf("\n");
    }
}