/*Πρόγραμμα 25 
Το πρόγραμμα διαβάζει από το πληκτρολόγιο χαρακτήρες και τους
ξαναγράφει στην οθόνη. Το τέλος εισαγωγής δεδομένων δηλώνεται με
το σύμβολο ('*') */

#include <stdio.h>
int main()
{
    int c;
    c = getchar();
    while (c != '*')
    {
        /* Η συνάρτηση putchar εμφανίζει στην οθόνη το
περιεχόμενο της ακέραιης μεταβλητής c σαν χαρακτήρα */
        putchar(c);
        c = getchar();
    }
}